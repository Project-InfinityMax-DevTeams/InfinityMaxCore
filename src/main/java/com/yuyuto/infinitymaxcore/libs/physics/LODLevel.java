package com.yuyuto.infinitymaxcore.libs.physics;

/**
 * <h2>LODLevel（Level Of Detail）</h2>
 *
 * <p>
 * 物理シミュレーションの計算精度レベルを表します。
 * </p>
 *
 * <h3>なぜ必要？</h3>
 *
 * リアル寄り物理は計算が非常に重いです。
 * すべてのセルをフル精度で計算すると、
 * ゲームが止まります。
 *
 * そこで「距離」や「重要度」に応じて
 * 計算レベルを切り替えます。
 *
 * 小学生向け説明：
 * 近くはくわしく計算。
 * 遠くはざっくり計算。
 */
public enum LODLevel {

    /**
     * セル単位の完全シミュレーション。
     * Navier-Stokes、熱伝導、相変化などを全て計算。
     *
     * → 最もリアル
     * → 最も重い
     */
    FULL,

    /**
     * 簡略物理モデル。
     * 圧力・温度のみ更新。
     *
     * → 中間精度
     */
    SIMPLIFIED,

    /**
     * チャンク単位で平均値のみ保持。
     *
     * → 超軽量
     * → 遠距離用
     */
    AVERAGED
}